function openArticleOptions() {
    document.getElementById('oaos').style.display = 'inherit';
    document.getElementById('OFFoaos').style.display = 'inherit';
    document.getElementById('ONoaos').style.display = 'none';
}

function closeArticleOptions() {
    document.getElementById('oaos').style.display = 'none';
    document.getElementById('OFFoaos').style.display = 'none';
    document.getElementById('ONoaos').style.display = 'inherit';

}